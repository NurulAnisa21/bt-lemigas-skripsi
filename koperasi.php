Koperasi Pegawai Lemigas terletak di dalam gedung Lemigas, beralamat di Jalan Ciledug Raya Kav. 109, Cipulir, Kebayoran Lama, Jakarta Selatan. Koperasi Pegawai Lemigas (KPL) didirikan pada tanggal 9 Juni 1980. Koperasi Karyawan Lemigas disahkan menjadi badan hukum dengan surat keputusan kantor wilayah koperasi DKI Jakarta pada tanggal 9 Juni 1980 dengan nomor : 1366/BH/I. <br><br>

Koperasi Pegawai Lemigas didirikan atas dasar himbauan pemerintah untuk mendirikan Koperasi disetiap instansi pemerintah. Karena itu, para pemimpin di Lemigas merasa berkewajiban untuk melaksanakan himbauan tersebut. Gagasan untuk mendirikan Koperasi Pegawai Lemigas dimulai sejak Organisasi Lemigas masih berstatus non pegawai negeri. <br><br>

Namun, realisasi terbentuknya dilaksanakan tidak lama setelah adanya perubahaan status organisasi Lemigas menjadi Pegawai Negeri Sipil dibawah Direktorat Jendral Migas, Departemen Pertambangan dan Energi dengan nama Koperasi Karyawan Lemigas (KKL). KPL melayani setiap kebutuhan anggota serta beberapa layanan yang diberikan bertujuan untuk kesejahteraan anggota. <br><br>

